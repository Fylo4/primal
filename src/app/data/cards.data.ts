type PrimalTag = "Essence to Hand" | 'Discard from my deck' | 'Discard from opponent deck';
type PrimalElement = "Water" | "Terra" | "Necro" | "Air" | "Fire" | "Plasma";
type PrimalCharacteristic = "Male" | "Female" | "Animal" | "Sea Monster" | "Weapon" | "Symbiote" | "Cayne Pirate" | "Three Aces" | "Vice Captain" | "Medical" | "Enchanter" | "Captain" | "Masked" | "Spirit" | "Therian" | "Draginked" | "Slayer" | "Android" | "Cyborg" | "Stone" | "Mercenary" | "Master" | 'Synthetic Life' | 'Zoltan' | 'Vampire' | 'Mechanic' | 'Pollux' | 'Mage';
type PrimalRarity = "Starter" | "Common" | "Uncommon" | "Rare" | "Super Rare" | "Promo";
type PrimalAttribute = "Micromon" | "Pleaguis" | "Dragon" | "Pirate" | "Overworld" | "Arizal" | 'Bio' | 'Kaiser' | 'Undead' | 'Bethesda';

export type PrimalCard = (PrimalCharacter | PrimalStrategy | PrimalAbility | PrimalField) & {
    printedName: string,
    name?: string,
    rarity: PrimalRarity,
    elements: PrimalElement[],
    tags?: PrimalTag[],
    imageUrls?: {url: string, width: number, height: number}[],
    synergies?: {selector: Function, text: string}[],
    cardNumber: string,
    set: string,
}

type PrimalCharacter = {
    type: "Character",
    turnCost: number,
    cardCost: number,
    leadHealthy: number,
    supportHealthy: number,
    leadInjured: number,
    supportInjured: number,
    attribute: PrimalAttribute,
    characteristics: PrimalCharacteristic[]
}
type PrimalStrategy = {
    type: "Strategy",
    turnCost: number,
    cardCost: number,
    isCounter?: boolean,
    permanent?: true | number,
}
type PrimalAbility = {
    type: "Ability",
    // 2D array = multiple cost options
    essenceCost: (PrimalElement | "Any")[] | (PrimalElement | "Any")[][],
    xCost?: boolean, // Has generic essence cost X applied to each cost option
}
type PrimalField = {
    type: "Field",
}

